# ── Helpers ─────────────────────────────────────────────────────────────────


def wb_warehouse_label(name: str) -> str:
    """Эмодзи по тарифному тиру (стоимость логистики, не частота)."""
    if name not in WB_FOOD_WAREHOUSES:
        return f"🔴 {name} (не 'питание')"
    tier = WB_COST_TIER.get(name, 0)
    emoji = _TIER_EMOJI.get(tier, "⚪")
    return f"{emoji} {name}"


def wb_cluster_names() -> List[str]:
    return list(WB_CLUSTERS.keys())


def wb_warehouses_in_cluster(cluster_name: str) -> List[str]:
    return WB_CLUSTERS.get(cluster_name, [])


def ozon_cluster_names() -> List[str]:
    return list(OZON_CLUSTERS.keys())


def ozon_warehouses_in_cluster(cluster_name: str) -> List[str]:
    return OZON_CLUSTERS.get(cluster_name, [])
